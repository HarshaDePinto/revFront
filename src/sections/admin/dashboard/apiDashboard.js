import { API } from "../../../config";

export const getAllRestaurants = (abortCont) => {
  return fetch(`${API}/restaurants`, {
    method: "GET",
    signal: abortCont.signal,
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => {
      if (err.name === "AbortError") {
        console.log("Fetch Aborted");
      } else {
        console.log(err);
      }
    });
};

  